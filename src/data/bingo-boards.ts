// Helper function to get a board by ID
export const getBoardById = (id: number): BingoBoard | undefined => {
  return bingoBoards.find((board) => board.id === id)
}

// Helper function to get a random board
export const getRandomBoard = (): BingoBoard => {
  const randomIndex = Math.floor(Math.random() * bingoBoards.length)
  return bingoBoards[randomIndex]
}
